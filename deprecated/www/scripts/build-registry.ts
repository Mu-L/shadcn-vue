// ----------------------------------------------------------------------------
// Sync styles
// ----------------------------------------------------------------------------
async function syncStyles() {
  const sourceStyle = 'new-york'
  const targetStyle = 'default'

  const syncDirectories = ['blocks', 'composables', 'internal', 'lib', 'charts']

  // Clean up sync directories.
  for (const dir of syncDirectories) {
    rimraf.sync(path.join('registry', targetStyle, dir))
  }

  for (const item of registry.items) {
    if (
      !REGISTRY_INDEX_WHITELIST.includes(item.type)
      && item.type !== 'registry:ui'
    ) {
      continue
    }

    const resolveFiles = item.files?.map(
      file =>
        `src/registry/${sourceStyle}/${typeof file === 'string' ? file : file.path}`,
    )
    if (!resolveFiles) {
      continue
    }

    // Copy files to target style if they don't exist.
    for (const file of resolveFiles) {
      const sourcePath = path.join(process.cwd(), file)
      const targetPath = path.join(
        process.cwd(),
        file.replace(sourceStyle, targetStyle),
      )

      if (!existsSync(targetPath)) {
        // Create directory if it doesn't exist.
        await fs.mkdir(path.dirname(targetPath), { recursive: true })
        await fs.copyFile(sourcePath, targetPath)

        // Replace all @/registry/new-york/ with @/registry/default/.
        const content = await fs.readFile(targetPath, 'utf8')
        const fixedContent = content.replace(
          new RegExp(`@/registry/${sourceStyle}/`, 'g'),
          `@/registry/${targetStyle}/`,
        )
        await fs.writeFile(targetPath, fixedContent, 'utf8')
      }
    }
  }
}

// ----------------------------------------------------------------------------
// Build __registry__/index.ts.
// ----------------------------------------------------------------------------
async function buildRegistry(registry: Registry) {
  let index = `/* eslint-disable ts/ban-ts-comment */
// @ts-nocheck
// This file is autogenerated by scripts/build-registry.ts
// Do not edit this file directly.

export const Index: Record<string, any> = {
`

  for (const style of styles) {
    const itemSet = new Set<string>()
    index += `  "${style.name}": {`

    // Build style index.
    for (const item of registry.items) {
      if (itemSet.has(item.name))
        continue
      itemSet.add(item.name)

      const resolveFiles = item.files?.map(
        file =>
          `registry/${style.name}/${
            typeof file === 'string' ? file : file.path
          }`,
      )
      if (!resolveFiles) {
        continue
      }

      // Validate categories.
      if (item.categories) {
        const invalidCategories = item.categories.filter(
          category => !registryCategories.some(c => c.slug === category),
        )

        if (invalidCategories.length > 0) {
          console.error(
            `${item.name} has invalid categories: ${invalidCategories}`,
          )
          process.exit(1)
        }
      }

      const type = item.type.split(':')[1]
      const sourceFilename = ''

      let componentPath = `@/registry/${style.name}/${type}/${item.name}`

      if (item.files) {
        const files = item.files.map(file =>
          typeof file === 'string'
            ? { type: 'registry:page', path: file }
            : file,
        )
        if (files?.length) {
          componentPath = `@/registry/${style.name}/${files[0]!.path}`
        }
      }

      index += `
    "${item.name}": {
      name: "${item.name}",
      description: "${item.description ?? ''}",
      type: "${item.type}",
      registryDependencies: ${JSON.stringify(item.registryDependencies)},
      files: [${item.files?.filter(file => !file.path.includes(METADATA_NAME))?.map((file) => {
        const filePath = `registry/${style.name}/${
          typeof file === 'string' ? file : file.path
        }`
        const resolvedFilePath = path.resolve(filePath)
        return typeof file === 'string'
          ? `"${resolvedFilePath}"`
          : `{
        path: "${filePath}",
        type: "${file.type}",
        target: "${file.target ?? ''}"
      }`
      })}],
      component: () => import("${componentPath}").then((m) => m.default),
      source: "${sourceFilename}",
      categories: ${JSON.stringify(item.categories ?? [])}
    },`
    }

    index += `
  },`
  }

  index += `
}
`

  // ----------------------------------------------------------------------------
  // Build registry/index.json.
  // ----------------------------------------------------------------------------
  const items = registry.items
    .filter((item: RegistryItem) => ['registry:ui'].includes(item.type))
    .map((item) => {
      return {
        ...item,
        files: item.files?.filter(file => !file.path.includes(METADATA_NAME))?.map((_file) => {
          const file = { path: _file.path, type: item.type }
          return file
        }),
      }
    })
  const registryJson = JSON.stringify(items, null, 2)
  rimraf.sync(path.join(REGISTRY_PATH, 'index.json'))
  await writeFile(
    path.join(REGISTRY_PATH, 'index.json'),
    registryJson,
  )

  // Write style index.
  rimraf.sync(path.join(process.cwd(), '__registry__/index.ts'))
  await writeFile(path.join(process.cwd(), '__registry__/index.ts'), index)
}

