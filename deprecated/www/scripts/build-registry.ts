async function generateRegistryFiles(allItems: RegistryItem[]) {
  // Separate items by type
  const ui = allItems.filter(item => item.type === 'registry:ui')
  const blocks = allItems.filter(item =>
    item.type === 'registry:block' && !item.name.includes('chart'),
  )
  const charts = allItems.filter(item =>
    item.type === 'registry:block' && item.name.includes('chart'),
  )
  const composables = allItems.filter(item => item.type === 'registry:composable')
  const examples = allItems.filter(item => item.type === 'registry:example')
  const lib = allItems.filter(item => item.type === 'registry:lib')

  // Generate registry files
  await Promise.all([
    generateRegistryFile('registry-ui.ts', 'ui', ui),
    generateRegistryFile('registry-blocks.ts', 'blocks', blocks),
    generateRegistryFile('registry-charts.ts', 'charts', charts),
    generateRegistryFile('registry-composables.ts', 'composables', composables),
    generateRegistryFile('registry-examples.ts', 'examples', examples),
    generateRegistryFile('registry-lib.ts', 'lib', lib),
  ])

  // eslint-disable-next-line no-console
  console.log('✅ Registry files generated successfully!')
  // eslint-disable-next-line no-console
  console.log(`📊 Generated ${allItems.length} registry items:`)
  // eslint-disable-next-line no-console
  console.log(`   - UI Components: ${ui.length}`)
  // eslint-disable-next-line no-console
  console.log(`   - Blocks: ${blocks.length}`)
  // eslint-disable-next-line no-console
  console.log(`   - Charts: ${charts.length}`)
  // eslint-disable-next-line no-console
  console.log(`   - Composables: ${composables.length}`)
  // eslint-disable-next-line no-console
  console.log(`   - Examples: ${examples.length}`)
  // eslint-disable-next-line no-console
  console.log(`   - Lib: ${lib.length}`)
}

